import math
 
# create function declarations
def add(a, b):
    return a + b
 
def subtract(a, b):
    return a - b
 
def divide(a, b):
    return a/b
 
def multiply(a, b):
    return a*b
 
def square(a):
    return a**2
 
def sqrt(a):
    return math.sqrt(a)
 
def log(a):
    return math.log(a)
 
def exponentiate(a, b):
    return a**b
